    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // boton para guardar los datos en la base de datos
        Serie prueba = new Serie();
        nombre = jTextField4.getText();
        numeroTemporadas = Integer.parseInt(jTextField2.getText());
        numeroEpisodios = Integer.parseInt(jTextField3.getText()); 
        prueba.setNombre(nombre);
        prueba.setNumeroCapitulos(numeroEpisodios);
        prueba.setNumeroTemporadas(numeroTemporadas);
        repo.save(prueba);
        jLabel8.setForeground(Color.green);
        jLabel8.setText("Serie guardada exitosamente!!!");
    }//GEN-LAST:event_jButton1ActionPerformed

