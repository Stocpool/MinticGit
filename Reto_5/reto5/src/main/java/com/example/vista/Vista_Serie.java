    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // boton para eliminar registros
        id = Integer.parseInt(jTextField1.getText());
        
        if(repo.existsById((long)id)){
            repo.deleteById((long)id);
            jLabel8.setForeground(Color.green);
            jLabel8.setText("Se ha eliminado la serie exitosamente!!");
        }else{
            jLabel8.setForeground(Color.red);
            jLabel8.setText("La serie con ese Id no existe :(");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // Boton para buscar en la base de datos
        id = Integer.parseInt(jTextField1.getText());
        Optional<Serie> searchedSerie;
        searchedSerie = repo.findById((long)id);
        Serie prueba;
        if(searchedSerie.isPresent()){
            prueba = searchedSerie.get();
            jTextField4.setText(prueba.getNombre());
            jTextField2.setText(String.valueOf(prueba.getNumeroTemporadas()));
            jTextField3.setText(String.valueOf(prueba.getNumeroCapitulos()));
            jLabel8.setForeground(Color.green);
            jLabel8.setText("Se ha encontrado la serie en la BD");
            
        }else{
            jLabel8.setForeground(Color.red);
            jLabel8.setText("No se ha encontrado la serie en la BD");
            jTextField4.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // Boton para actualizar un dato
        id = Integer.parseInt(jTextField1.getText());
        Optional<Serie> searchedSerie;
        searchedSerie = repo.findById((long)id);
        Serie prueba;
        if(searchedSerie.isPresent()){
            prueba = searchedSerie.get();
            nombre = jTextField4.getText();
            numeroTemporadas=Integer.parseInt(jTextField2.getText());
            numeroEpisodios = Integer.parseInt(jTextField3.getText());
            prueba.setNombre(nombre);
            prueba.setNumeroCapitulos(numeroEpisodios);
            prueba.setNumeroTemporadas(numeroTemporadas);
            repo.save(prueba);
            jLabel8.setForeground(Color.green);
            jLabel8.setText("Se ha actualizado el el registro correctamente");
            
        }else{
            jLabel8.setForeground(Color.red);
            jLabel8.setText("No se ha podido actualizar");
            jTextField4.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

