    private void botonInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonInsertarActionPerformed
        // TODO add your handling code here:
        int numeroRegistro = Integer.parseInt(campoNumeroRegistro.getText());
        int capacidad = Integer.parseInt(campoCapacidad.getText());
        String modelo = CampoModelo.getText();
        Avion nuevoAvion = new Avion(numeroRegistro, modelo, capacidad);
        repositorioAvion.save(nuevoAvion);
        mensajes.setText("Avion creado correctamente");
        
    }//GEN-LAST:event_botonInsertarActionPerformed

