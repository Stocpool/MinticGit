    private void botonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonBuscarActionPerformed
        // TODO add your handling code here:
        int numeroRegistro = Integer.parseInt(campoNumeroRegistro.getText());
        
        Optional<Avion> resultado = repositorioAvion.findById(numeroRegistro);
        if (resultado.isPresent()) {
            Avion avionEncontrado = resultado.get();
            campoCapacidad.setText(avionEncontrado.getCapacidad().toString());
            CampoModelo.setText(avionEncontrado.getModelo());
        } else {
            mensajes.setText("Número registro inexistente");
        }
        
    }//GEN-LAST:event_botonBuscarActionPerformed

